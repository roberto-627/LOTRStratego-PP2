    private void btn_eliminarcuentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarcuentaActionPerformed
    LoginMenu LogIn = new LoginMenu();
    int sure = JOptionPane.showConfirmDialog(null, "Esta seguro en eliminar cuenta?", "Eliminar Cuenta", JOptionPane.YES_NO_OPTION);
        if(sure == JOptionPane.YES_OPTION){
            Player.players.remove(LoginMenu.currentPlayer);
            JOptionPane.showMessageDialog(null, "Usuario eliminado exitosamente!!");
            
            MyProfile_Pane.setVisible(false);
            MainMenu_Pane.setVisible(false);
            LogIn.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btn_eliminarcuentaActionPerformed

