    private void btn_newPassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_newPassActionPerformed
        
        if(txtnewPass.getPassword().length ==0){
            JOptionPane.showMessageDialog(null, "Porfavor ingrese nueva contraseña!!");
        }
        if(txtnewPass.getPassword().length>5){
            JOptionPane.showMessageDialog(null, "Contraseña no puede ser mayor de 5 caracteres!!");
            txtnewPass.setText("");       
        }else{       
        int r = JOptionPane.showConfirmDialog(null, "Esta seguro de cambiar la contraseña?", "Cambiar Contraseña", JOptionPane.YES_NO_OPTION);
            if(r == JOptionPane.YES_OPTION){
               LoginMenu.currentPlayer.setPassword(String.valueOf(txtnewPass.getPassword()));
               txtnewPass.setText("");
               CambiarPass.setVisible(false);
               MyProfile_Pane.setVisible(true);
        }else{
            txtnewPass.setText("");
            CambiarPass.setVisible(false);
            MyProfile_Pane.setVisible(true);
            
        }
       }
    }//GEN-LAST:event_btn_newPassActionPerformed

